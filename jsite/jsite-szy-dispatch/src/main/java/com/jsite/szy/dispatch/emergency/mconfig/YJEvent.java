package com.jsite.szy.dispatch.emergency.mconfig;

public class YJEvent {

	private String eid;
	
	private String stcd;
	
	private String allItem;
	
	private String stm;
	
	private String etm;

	public String getEid() {
		return eid;
	}

	public void setEid(String eid) {
		this.eid = eid;
	}

	public String getStcd() {
		return stcd;
	}

	public void setStcd(String stcd) {
		this.stcd = stcd;
	}

	public String getAllItem() {
		return allItem;
	}

	public void setAllItem(String allItem) {
		this.allItem = allItem;
	}

	public String getStm() {
		return stm;
	}

	public void setStm(String stm) {
		this.stm = stm;
	}

	public String getEtm() {
		return etm;
	}

	public void setEtm(String etm) {
		this.etm = etm;
	}
	
	
}
